#!/usr/bin/env bash
# =============================================================================
# check.sh — 프로젝트 검증 스크립트
#
# Claude Code Stop hook에서 자동 실행된다.
# AI가 작업을 완료할 때마다 이 스크립트가 실행되어 코드 품질을 검증한다.
# 이 스크립트가 exit 1을 반환하면 Claude Code가 작업을 계속하지 않는다.
#
# 작성 규칙:
#   1. 린트 → 빌드 → 테스트 순서로 실행한다.
#   2. 각 커맨드는 실패 시 즉시 중단되도록 set -e 를 유지한다.
#   3. 테스트는 watch 모드 없이 1회 실행 후 종료되는 커맨드를 사용한다.
#   4. 각 커맨드 뒤에 무엇을 검증하는지 주석으로 적는다.
#   5. 외부 서비스(DB, API 서버 등)가 필요한 테스트는 여기서 실행하지 않는다.
#      해당 테스트는 별도 스크립트로 분리하거나 CI에서만 실행한다.
#
# 예시:
#   npm run lint 2>&1        # 코드 스타일 및 정적 분석
#   npm run build 2>&1       # 컴파일 에러 없음 확인
#   npm test -- --watchAll=false 2>&1  # 단위 테스트 통과
# =============================================================================

set -e

# TODO: 아래에 이 프로젝트의 린트/빌드/테스트 커맨드를 작성하라.
# CLAUDE.md 의 "명령어" 섹션에 있는 커맨드를 참고한다.

echo "ERROR: scripts/check.sh 에 커맨드가 작성되지 않았습니다." >&2
echo "       CLAUDE.md 의 명령어 섹션을 참고해 린트/빌드/테스트 커맨드를 추가하세요." >&2
exit 1
